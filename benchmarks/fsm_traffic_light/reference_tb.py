"""
reference_tb.py
Hand-written reference testbench for traffic light FSM.
Ground truth -- never regenerated by LLM.

RTL parameters (verified against reference_rtl.v):
  State encoding: RED=0, GREEN=1, YELLOW=2
  RED_COUNT    = 14
  GREEN_COUNT  = 19
  YELLOW_COUNT = 4

State transitions happen on the RisingEdge of clk.
The counter resets to 0 on transition edge.

VPI workaround: Icarus 11.0 + cocotb 2.0.1 does not propagate NBA-updated
registered outputs to VPI immediately after RisingEdge returns. A 1ps delay
(0.01% of 10ns clock) before reading signals ensures correct VPI values.
"""

import cocotb
from cocotb.clock import Clock
from cocotb.triggers import RisingEdge, Timer

# State encoding matching reference_rtl.v
RED    = 0
GREEN  = 1
YELLOW = 2

# Timing parameters matching reference_rtl.v
RED_COUNT    = 14
GREEN_COUNT  = 19
YELLOW_COUNT = 4


async def do_reset(dut):
    """Assert async reset, wait 3 clocks, deassert."""
    dut.rst_n.value = 0
    dut.red_light.value = 0   # force all outputs low during reset
    await RisingEdge(dut.clk)  # reset active
    await RisingEdge(dut.clk)  # still reset
    await RisingEdge(dut.clk)  # still reset
    dut.rst_n.value = 1        # deassert after edge 3
    await RisingEdge(dut.clk)  # edge 4: stable with rst_n=1


async def read_state(dut):
    """Read state_out after a RisingEdge with VPI safety delay."""
    await RisingEdge(dut.clk)
    await Timer(1, unit='ps')
    return int(dut.state_out.value)


async def check_state(dut, expected, msg=""):
    """Wait for RisingEdge, then check state_out == expected."""
    state = await read_state(dut)
    assert state == expected, (
        f"{msg}: expected state {expected}, got {state}"
    )


@cocotb.test()
async def test_reset_initial_state(dut):
    """After reset, verify RED state with correct output signals."""
    clock = Clock(dut.clk, 10, unit='ns')
    cocotb.start_soon(clock.start())

    dut.rst_n.value = 0
    await RisingEdge(dut.clk)
    await RisingEdge(dut.clk)
    await RisingEdge(dut.clk)
    dut.rst_n.value = 1
    await RisingEdge(dut.clk)

    await Timer(1, unit='ps')

    assert int(dut.state_out.value) == RED, (
        f"Initial state should be RED(0), got {int(dut.state_out.value)}"
    )
    assert int(dut.red_light.value) == 1, "red_light should be 1 in RED state"
    assert int(dut.green_light.value) == 0, "green_light should be 0 in RED state"
    assert int(dut.yellow_light.value) == 0, "yellow_light should be 0 in RED state"


@cocotb.test()
async def test_red_to_green_transition(dut):
    """
    Verify RED state holds for RED_COUNT=14 cycles,
    then transitions to GREEN at the 14th RisingEdge.
    """
    clock = Clock(dut.clk, 10, unit='ns')
    cocotb.start_soon(clock.start())

    # Reset
    dut.rst_n.value = 0
    await RisingEdge(dut.clk)
    await RisingEdge(dut.clk)
    await RisingEdge(dut.clk)
    dut.rst_n.value = 1
    await RisingEdge(dut.clk)
    await Timer(1, unit='ps')

    # This first RisingEdge after reset starts the counter (counter=0->1, state RED)
    # Edge 1: state RED, counter becomes 1
    assert int(dut.state_out.value) == RED, "Should be RED at first edge"
    assert int(dut.red_light.value) == 1

    # Stay RED for RED_COUNT-2 more edges (edges 2 through 13)
    for i in range(RED_COUNT - 2):
        await RisingEdge(dut.clk)
        await Timer(1, unit='ps')
        assert int(dut.state_out.value) == RED, (
            f"Expected RED at edge {i + 2}, got {int(dut.state_out.value)}"
        )
        assert int(dut.red_light.value) == 1

    # RED_COUNT-th RisingEdge triggers transition: counter was RED_COUNT-1=13
    # before edge, so next_state=GREEN. After edge: state=GREEN, counter=0.
    await RisingEdge(dut.clk)
    await Timer(1, unit='ps')
    assert int(dut.state_out.value) == GREEN, (
        f"Expected GREEN(1) after {RED_COUNT} edges, "
        f"got {int(dut.state_out.value)}"
    )
    assert int(dut.green_light.value) == 1
    assert int(dut.red_light.value) == 0


@cocotb.test()
async def test_green_to_yellow_transition(dut):
    """
    Verify GREEN state holds for GREEN_COUNT=19 cycles,
    then transitions to YELLOW.
    """
    clock = Clock(dut.clk, 10, unit='ns')
    cocotb.start_soon(clock.start())

    # Reset
    dut.rst_n.value = 0
    await RisingEdge(dut.clk)
    await RisingEdge(dut.clk)
    await RisingEdge(dut.clk)
    dut.rst_n.value = 1

    # Fast-forward through RED state (RED_COUNT edges)
    for _ in range(RED_COUNT):
        await RisingEdge(dut.clk)
    await Timer(1, unit='ps')

    # Now in GREEN
    assert int(dut.state_out.value) == GREEN, "Should be in GREEN after RED"

    # Stay GREEN for GREEN_COUNT-1 edges (edges where state remains GREEN)
    for i in range(GREEN_COUNT - 1):
        await RisingEdge(dut.clk)
        await Timer(1, unit='ps')
        assert int(dut.state_out.value) == GREEN, (
            f"Expected GREEN at GREEN edge {i + 1}, "
            f"got {int(dut.state_out.value)}"
        )
        assert int(dut.green_light.value) == 1

    # This RisingEdge transitions to YELLOW (counter reached GREEN_COUNT-1=18)
    await RisingEdge(dut.clk)
    await Timer(1, unit='ps')
    assert int(dut.state_out.value) == YELLOW, (
        f"Expected YELLOW(2) after GREEN, got {int(dut.state_out.value)}"
    )
    assert int(dut.yellow_light.value) == 1
    assert int(dut.green_light.value) == 0


@cocotb.test()
async def test_yellow_to_red_transition(dut):
    """
    Verify YELLOW state holds for YELLOW_COUNT=4 cycles,
    then transitions back to RED (completing the loop).
    """
    clock = Clock(dut.clk, 10, unit='ns')
    cocotb.start_soon(clock.start())

    # Reset
    dut.rst_n.value = 0
    await RisingEdge(dut.clk)
    await RisingEdge(dut.clk)
    await RisingEdge(dut.clk)
    dut.rst_n.value = 1

    # Fast-forward through RED (14 edges) and GREEN (19 edges)
    for _ in range(RED_COUNT + GREEN_COUNT):
        await RisingEdge(dut.clk)
    await Timer(1, unit='ps')

    # Now in YELLOW
    assert int(dut.state_out.value) == YELLOW, "Should be in YELLOW after GREEN"

    # Stay YELLOW for YELLOW_COUNT-1 edges
    for i in range(YELLOW_COUNT - 1):
        await RisingEdge(dut.clk)
        await Timer(1, unit='ps')
        assert int(dut.state_out.value) == YELLOW, (
            f"Expected YELLOW at YELLOW edge {i + 1}, "
            f"got {int(dut.state_out.value)}"
        )
        assert int(dut.yellow_light.value) == 1

    # This edge transitions back to RED
    await RisingEdge(dut.clk)
    await Timer(1, unit='ps')
    assert int(dut.state_out.value) == RED, (
        f"Expected RED(0) completing the cycle, "
        f"got {int(dut.state_out.value)}"
    )
    assert int(dut.red_light.value) == 1
    assert int(dut.yellow_light.value) == 0
