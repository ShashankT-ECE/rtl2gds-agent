@cocotb.test()
async def test_reset(dut):
    """
    After reset, verify:
      - empty=1, full=0, count=0
      - wr_en, rd_en, din are deasserted cleanly
    """
    clock = Clock(dut.clk, 10, unit='ns')
    cocotb.start_soon(clock.start())

    # Assert reset -- no prior RisingEdge to wait for at time 0
    dut.rst_n.value = 0
    dut.wr_en.value = 0
    dut.rd_en.value = 0
    dut.din.value = 0

    await RisingEdge(dut.clk)   # edge 1: reset active, count forced to 0
    await RisingEdge(dut.clk)   # edge 2: reset still active
    await RisingEdge(dut.clk)   # edge 3: reset still active
    dut.rst_n.value = 1         # deassert reset after edge 3

    await RisingEdge(dut.clk)   # edge 4: stable with rst_n=1

    assert int(dut.empty.value) == 1, "FIFO should be empty after reset"
    assert int(dut.full.value) == 0, "FIFO should not be full after reset"
    assert int(dut.count.value) == 0, "Count should be 0 after reset"


@cocotb.test()
async def test_write_fourteen_not_full(dut):
    """
    Write 14 entries. After each write, verify full=0.
    Full asserts at count==15, so 14 writes leaves count=14, full=0.
    """
    clock = Clock(dut.clk, 10, unit='ns')
    cocotb.start_soon(clock.start())

    # Reset
    dut.rst_n.value = 0
    await RisingEdge(dut.clk)
    await RisingEdge(dut.clk)
    await RisingEdge(dut.clk)
    dut.rst_n.value = 1
    await RisingEdge(dut.clk)

    # Write 14 entries -- after each, check full should still be 0
    for i in range(14):
        await write_word(dut, 0xA0 + i)
        await RisingEdge(dut.clk)       # extra edge for VPI safety
        assert int(dut.full.value) == 0, (
            f"full asserted early at write {i + 1} "
            f"(count={int(dut.count.value)})"
        )

    assert int(dut.count.value) == 14, (
        f"Expected count=14 after 14 writes, got {int(dut.count.value)}"
    )


@cocotb.test()
async def test_fifteenth_write_full(dut):
    """
    Write the 15th entry and verify full=1.
    """
    clock = Clock(dut.clk, 10, unit='ns')
    cocotb.start_soon(clock.start())

    # Reset
    dut.rst_n.value = 0
    await RisingEdge(dut.clk)
    await RisingEdge(dut.clk)
    await RisingEdge(dut.clk)
    dut.rst_n.value = 1
    await RisingEdge(dut.clk)

    # Write 14 entries first
    for i in range(14):
        await write_word(dut, 0xA0 + i)
        await RisingEdge(dut.clk)

    # Write 15th entry -- count becomes 15, full=1
    await write_word(dut, 0xAE)
    await RisingEdge(dut.clk)           # extra edge for VPI safety

    assert int(dut.full.value) == 1, "full should be 1 after 15 writes"
    assert int(dut.count.value) == 15, (
        f"Expected count=15, got {int(dut.count.value)}"
    )


@cocotb.test()
async def test_write_when_full_blocked(dut):
    """
    After full=1, attempt another write. Verify:
      - full stays 1
      - count unchanged (write blocked because wr_en && !full evaluates false)
    """
    clock = Clock(dut.clk, 10, unit='ns')
    cocotb.start_soon(clock.start())

    # Reset
    dut.rst_n.value = 0
    await RisingEdge(dut.clk)
    await RisingEdge(dut.clk)
    await RisingEdge(dut.clk)
    dut.rst_n.value = 1
    await RisingEdge(dut.clk)

    # Fill FIFO (15 writes)
    for i in range(15):
        await write_word(dut, i)
        await RisingEdge(dut.clk)

    assert int(dut.full.value) == 1, "FIFO should be full after 15 writes"
    last_count = int(dut.count.value)
    assert last_count == 15, f"Expected count=15, got {last_count}"

    # Attempt write when full -- should be blocked (wr_en && !full is false)
    await write_word(dut, 0xFF)
    await RisingEdge(dut.clk)

    assert int(dut.full.value) == 1, "full should remain 1 after blocked write"
    assert int(dut.count.value) == last_count, (
        f"count should not change after blocked write "
        f"(was {last_count}, now {int(dut.count.value)})"
    )


@cocotb.test()
async def test_read_all_fifteen(dut):
    """
    Write 15 entries, read all 15 back, verify data order matches write order.
    After last read, verify empty=1 and count=0.
    """
    clock = Clock(dut.clk, 10, unit='ns')
    cocotb.start_soon(clock.start())

    # Reset
    dut.rst_n.value = 0
    await RisingEdge(dut.clk)
    await RisingEdge(dut.clk)
    await RisingEdge(dut.clk)
    dut.rst_n.value = 1
    await RisingEdge(dut.clk)

    # Write 15 entries with known pattern
    write_data = [0xA0 + i for i in range(15)]
    for val in write_data:
        await write_word(dut, val)
        await RisingEdge(dut.clk)

    assert int(dut.full.value) == 1, "FIFO should be full after 15 writes"
    assert int(dut.count.value) == 15, f"Expected count=15, got {int(dut.count.value)}"

    # Read all 15 entries back -- verify order matches
    for i, expected in enumerate(write_data):
        actual = await read_word(dut)
        await RisingEdge(dut.clk)       # extra edge for VPI safety
        assert actual == expected, (
            f"Read mismatch at index {i}: "
            f"expected 0x{expected:02X}, got 0x{actual:02X}"
        )

    # After all reads, FIFO should be empty
    await RisingEdge(dut.clk)
    assert int(dut.empty.value) == 1, "FIFO should be empty after reading all entries"
    assert int(dut.count.value) == 0, (
        f"count should be 0 after reading all entries, got {int(dut.count.value)}"
    )


@cocotb.test()
async def test_simultaneous_rw(dut):
    """
    Test simultaneous read+write when FIFO has entries.
    Write new data while reading old data in the same clock cycle.
    Verify count stays unchanged (one read + one write cancel out).
    dout should show the OLD data (read-before-write semantics in the RTL).
    """
    clock = Clock(dut.clk, 10, unit='ns')
    cocotb.start_soon(clock.start())

    # Reset
    dut.rst_n.value = 0
    await RisingEdge(dut.clk)
    await RisingEdge(dut.clk)
    await RisingEdge(dut.clk)
    dut.rst_n.value = 1
    await RisingEdge(dut.clk)

    # Write 5 entries: [0x50, 0x51, 0x52, 0x53, 0x54]
    for i in range(5):
        await write_word(dut, 0x50 + i)
        await RisingEdge(dut.clk)

    assert int(dut.count.value) == 5, f"Expected count=5, got {int(dut.count.value)}"

    # Simultaneous: read at rd_ptr=0 (0x50), write 0xAA at wr_ptr=5
    # Drive both rd_en and wr_en AFTER synchronization edge
    await RisingEdge(dut.clk)       # synchronize
    dut.wr_en.value = 1
    dut.rd_en.value = 1
    dut.din.value = 0xAA
    await RisingEdge(dut.clk)       # both ops happen here
    # dout registered: rd_ptr[3:0]=0, wr_ptr[3:0]=5 (no forwarding)
    # dout should be mem[0] = 0x50
    await Timer(1, unit='ps')       # let VPI propagate NBA-updated dout
    data = int(dut.dout.value)
    dut.wr_en.value = 0             # deassert after edge
    dut.rd_en.value = 0

    # count should stay at 5 (one read + one write)
    await RisingEdge(dut.clk)
    assert int(dut.count.value) == 5, (
        f"count should be 5 after simultaneous R+W, got {int(dut.count.value)}"
    )

    # Read data should be old data at rd_ptr=0: 0x50
    # (forwarding check: wr_ptr[3:0]=5 != rd_ptr[3:0]=0, so no forwarding)
    assert data == 0x50, (
        f"Simultaneous R+W: expected old data 0x50, got 0x{data:02X}"
    )

    # Read remaining original entries: 0x51, 0x52, 0x53, 0x54
    for expected in [0x51, 0x52, 0x53, 0x54]:
        actual = await read_word(dut)
        await RisingEdge(dut.clk)
        assert actual == expected, (
            f"After simultaneous R+W: "
            f"expected 0x{expected:02X}, got 0x{actual:02X}"
        )

    # The simultaneous write added 0xAA at mem[5], so one entry remains.
    # Read it to verify the write happened correctly.
    actual = await read_word(dut)
    await RisingEdge(dut.clk)
    assert actual == 0xAA, (
        f"Simultaneous write data: expected 0xAA, got 0x{actual:02X}"
    )

    # FIFO should now be empty (count=0)
    await RisingEdge(dut.clk)
    assert int(dut.empty.value) == 1, "FIFO should be empty after reading all entries"
    assert int(dut.count.value) == 0, (
        f"Expected count=0, got {int(dut.count.value)}"
    )
