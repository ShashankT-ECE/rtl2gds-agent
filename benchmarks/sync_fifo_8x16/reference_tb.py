"""
reference_tb.py
Hand-written reference testbench for synchronous FIFO (sync_fifo_8x16).
Ground truth -- never regenerated by LLM.

RTL parameters (verified against reference_rtl.v):
  - Full asserts when count == 15 (15-entry depth, NOT 16)
  - Width: 8 bits
  - full/empty are combinational outputs (continuous assign)
  - dout is registered (one cycle latency, non-blocking assignment)

CRITICAL -- correct cocotb 2.x synchronous pattern:
  - NEVER drive signals before or at the same simulation time as a RisingEdge
  - Always drive signals IMMEDIATELY AFTER a RisingEdge completes
  - Read registered outputs (dout) AFTER the RisingEdge that produces them
  - Read combinatorial outputs (full, empty) AFTER the RisingEdge that changes them

This pattern avoids the VPI race condition where signals driven right before
a RisingEdge may not be sampled by the simulator's always @(posedge clk) blocks.
"""

import cocotb
from cocotb.clock import Clock
from cocotb.triggers import RisingEdge, Timer


async def write_word(dut, data):
    """Write one word to FIFO. Drives signals AFTER RisingEdge."""
    await RisingEdge(dut.clk)       # synchronize to clock
    dut.wr_en.value = 1             # drive after edge -- settles before next edge
    dut.din.value = data
    dut.rd_en.value = 0             # ensure clean write-only operation
    await RisingEdge(dut.clk)       # write happens on this edge -- DUT sees wr_en=1
    dut.wr_en.value = 0             # deassert after edge


async def read_word(dut):
    """Read one word from FIFO. Returns data after it's stable.

    NOTE: The Timer(1ps) after the read RisingEdge is required because Icarus
    Verilog 11.0 + cocotb 2.0.1 does not propagate NBA-updated dout to VPI
    immediately after RisingEdge returns. A 1ps delay (0.01% of clock period)
    is negligible but ensures VPI reads return the correct registered value.
    """
    await RisingEdge(dut.clk)       # synchronize to clock
    dut.rd_en.value = 1             # drive after edge
    dut.wr_en.value = 0             # ensure clean read-only operation
    await RisingEdge(dut.clk)       # read happens -- dout registered, updates here
    await Timer(1, unit='ps')       # let VPI propagate NBA-updated dout
    data = int(dut.dout.value)      # read registered output after edge
    dut.rd_en.value = 0             # deassert after edge
    return data


