"""
reference_tb.py
Hand-written reference testbench for 8-bit ALU.
Ground truth — never regenerated by LLM.

The ALU is a purely combinational design with no clock.
All tests use Timer() for propagation delay.
"""

import cocotb
from cocotb.triggers import Timer


def reference_model(A: int, B: int, opcode: int) -> tuple:
    """
    Pure Python reference model of the ALU.

    Args:
        A: 8-bit input A
        B: 8-bit input B
        opcode: 3-bit operation code (0-7)

    Returns:
        (Y, zero_flag) where Y is 8-bit result, zero_flag is 0 or 1
    """
    if opcode == 0:      # ADD
        Y = (A + B) & 0xFF
    elif opcode == 1:    # SUB
        Y = (A - B) & 0xFF
    elif opcode == 2:    # AND
        Y = A & B
    elif opcode == 3:    # OR
        Y = A | B
    elif opcode == 4:    # XOR
        Y = A ^ B
    elif opcode == 5:    # NOT
        Y = (~A) & 0xFF
    elif opcode == 6:    # SHL
        Y = (A << 1) & 0xFF
    elif opcode == 7:    # SHR
        Y = (A >> 1) & 0xFF
    else:
        Y = 0

    zero_flag = 1 if Y == 0 else 0
    return Y, zero_flag


OPCODE_NAMES = ["ADD", "SUB", "AND", "OR", "XOR", "NOT", "SHL", "SHR"]


@cocotb.test()
async def test_all_opcodes_boundary_values(dut):
    """
    Test all 8 opcodes with boundary value combinations.
    Boundary values: 0x00, 0xFF, 0x01, 0x80, 0x7F
    """
    boundaries = [0x00, 0xFF, 0x01, 0x80, 0x7F]
    for opcode in range(8):
        for A_val in boundaries:
            for B_val in boundaries:
                dut.A.value = A_val
                dut.B.value = B_val
                dut.opcode.value = opcode
                await Timer(1, unit='ns')

                expected_Y, expected_zf = reference_model(A_val, B_val, opcode)
                actual_Y = int(dut.Y.value)
                actual_zf = int(dut.zero_flag.value)

                assert actual_Y == expected_Y, (
                    f"{OPCODE_NAMES[opcode]} mismatch: "
                    f"A=0x{A_val:02X} B=0x{B_val:02X} -> "
                    f"Y=0x{actual_Y:02X} expected 0x{expected_Y:02X}"
                )
                assert actual_zf == expected_zf, (
                    f"{OPCODE_NAMES[opcode]} zero_flag mismatch: "
                    f"got {actual_zf} expected {expected_zf} "
                    f"for Y=0x{actual_Y:02X}"
                )


@cocotb.test()
async def test_additional_edge_cases(dut):
    """
    Test additional edge cases not covered by boundary value matrix:
    - Alternating bit patterns
    - Values that produce zero results for each opcode
    """
    extra_pairs = [
        (0xAA, 0x55, 0, "ADD alternate bits"),
        (0x55, 0xAA, 0, "ADD alternate bits swapped"),
        (0x00, 0x00, 0, "ADD zero + zero"),
        (0xFF, 0x01, 1, "SUB max - 1"),
        (0x00, 0x01, 1, "SUB 0 - 1 -> 0xFF"),
        (0xFF, 0x0F, 2, "AND bitmask"),
        (0xF0, 0x0F, 3, "OR combine"),
        (0xFF, 0xFF, 4, "XOR same -> 0x00"),
        (0xFF, 0x00, 5, "NOT FF -> 0x00"),
        (0x80, 0x00, 6, "SHL 0x80 -> 0x00 (overflow)"),
        (0x01, 0x00, 6, "SHL 0x01 -> 0x02"),
        (0x02, 0x00, 7, "SHR 0x02 -> 0x01"),
        (0x80, 0x00, 7, "SHR 0x80 -> 0x40"),
    ]

    for A_val, B_val, opcode, description in extra_pairs:
        dut.A.value = A_val
        dut.B.value = B_val
        dut.opcode.value = opcode
        await Timer(1, unit='ns')

        expected_Y, expected_zf = reference_model(A_val, B_val, opcode)
        actual_Y = int(dut.Y.value)
        actual_zf = int(dut.zero_flag.value)

        assert actual_Y == expected_Y, (
            f"{description}: Y=0x{actual_Y:02X} expected 0x{expected_Y:02X}"
        )
        assert actual_zf == expected_zf, (
            f"{description}: zero_flag={actual_zf} expected {expected_zf}"
        )


@cocotb.test()
async def test_zero_flag_asserts(dut):
    """
    Verify zero_flag=1 for operations that produce Y=0x00.
    Tests each opcode with values known to produce zero.
    """
    zero_cases = [
        (0x00, 0x00, 0, "ADD 0+0"),
        (0x01, 0x01, 1, "SUB 1-1"),
        (0x00, 0xFF, 2, "AND 0&FF"),
        (0x00, 0x00, 3, "OR 0|0"),
        (0xFF, 0xFF, 4, "XOR FF^FF"),
        (0xFF, 0x00, 5, "NOT ~FF"),
        (0x00, 0x00, 6, "SHL 0"),
        (0x00, 0x00, 7, "SHR 0"),
    ]

    for A_val, B_val, opcode, description in zero_cases:
        dut.A.value = A_val
        dut.B.value = B_val
        dut.opcode.value = opcode
        await Timer(1, unit='ns')

        expected_Y, expected_zf = reference_model(A_val, B_val, opcode)
        actual_Y = int(dut.Y.value)
        actual_zf = int(dut.zero_flag.value)

        assert actual_Y == 0, f"{description}: expected Y=0x00 got 0x{actual_Y:02X}"
        assert expected_zf == 1, f"{description}: reference model should predict zero_flag=1"
        assert actual_zf == 1, (
            f"{description}: zero_flag not asserted — got {actual_zf}"
        )


@cocotb.test()
async def test_zero_flag_not_asserted(dut):
    """
    Verify zero_flag=0 for operations that produce Y != 0x00.
    """
    nonzero_cases = [
        (0x01, 0x02, 0, "ADD 1+2=3"),
        (0x05, 0x03, 1, "SUB 5-3=2"),
        (0xFF, 0x0F, 2, "AND FF&0F=0F"),
        (0xF0, 0x0F, 3, "OR F0|0F=FF"),
        (0xFF, 0x00, 4, "XOR FF^00=FF"),
        (0x7F, 0x00, 5, "NOT ~7F=80"),
        (0x01, 0x00, 6, "SHL 1<<1=2"),
        (0x02, 0x00, 7, "SHR 2>>1=1"),
    ]

    for A_val, B_val, opcode, description in nonzero_cases:
        dut.A.value = A_val
        dut.B.value = B_val
        dut.opcode.value = opcode
        await Timer(1, unit='ns')

        expected_Y, expected_zf = reference_model(A_val, B_val, opcode)
        actual_Y = int(dut.Y.value)
        actual_zf = int(dut.zero_flag.value)

        assert actual_Y != 0, f"{description}: expected nonzero Y got 0x00"
        assert expected_zf == 0, f"{description}: reference model should predict zero_flag=0"
        assert actual_zf == 0, (
            f"{description}: zero_flag should be 0 for nonzero result — got {actual_zf}"
        )
