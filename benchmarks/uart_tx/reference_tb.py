"""
reference_tb.py
Hand-written reference testbench for UART Transmitter (uart_tx).
Ground truth -- never regenerated by LLM.

CRITICAL -- correct cocotb 2.x synchronous pattern:
  - Drive signals IMMEDIATELY AFTER a RisingEdge, never before
  - Use Timer(1, unit='ps') after RisingEdge for VPI settling
"""
import cocotb
from cocotb.clock import Clock
from cocotb.triggers import RisingEdge, Timer

BAUD_DIV = 20  # 20 clk cycles per baud bit for test purposes
HALF_BAUD = 10  # half baud period for sampling midpoint

async def send_byte(dut, data, baud_div=BAUD_DIV):
    """Send a byte through the UART TX by pulsing tx_start."""
    await RisingEdge(dut.clk)
    dut.tx_start.value = 1
    dut.tx_data.value = data
    dut.baud_div.value = baud_div
    await RisingEdge(dut.clk)
    dut.tx_start.value = 0

async def uart_monitor(dut, baud_div=BAUD_DIV):
    """Monitor the tx line and return the received byte.
    Waits for start bit, samples 8 data bits at midpoint, checks stop bit."""
    # Wait for start bit (tx goes low)
    while int(dut.tx.value) != 0:
        await RisingEdge(dut.clk)

    # Wait half baud to sample at midpoint of start bit
    for _ in range(HALF_BAUD):
        await RisingEdge(dut.clk)

    # Sample 8 data bits at midpoint of each baud period
    data = 0
    for i in range(8):
        # Wait full baud period to reach midpoint of next bit
        for _ in range(baud_div):
            await RisingEdge(dut.clk)
        bit = int(dut.tx.value)
        data |= (bit << i)

    # Wait for stop bit (should be high)
    for _ in range(baud_div):
        await RisingEdge(dut.clk)
    # Stop bit sampled, return data
    return data

@cocotb.test()
async def test_reset(dut):
    """After reset, verify tx=1 and tx_busy=0."""
    clock = Clock(dut.clk, 10, unit='ns')
    cocotb.start_soon(clock.start())

    dut.rst_n.value = 0
    dut.tx_start.value = 0
    dut.tx_data.value = 0
    dut.baud_div.value = BAUD_DIV
    await RisingEdge(dut.clk)
    await RisingEdge(dut.clk)
    await RisingEdge(dut.clk)
    assert int(dut.tx.value) == 1, "tx should be 1 after reset"
    assert int(dut.tx_busy.value) == 0, "tx_busy should be 0 after reset"

@cocotb.test()
async def test_send_0x55(dut):
    """Send 0x55 (alternating 01010101), verify each bit on tx line at correct baud timing."""
    clock = Clock(dut.clk, 10, unit='ns')
    cocotb.start_soon(clock.start())

    dut.rst_n.value = 0
    await RisingEdge(dut.clk)
    await RisingEdge(dut.clk)
    await RisingEdge(dut.clk)
    dut.rst_n.value = 1
    await RisingEdge(dut.clk)

    # Send 0x55 (0b01010101, LSB first: 1,0,1,0,1,0,1,0)
    await send_byte(dut, 0x55)

    # We expect: start=0, bits=1,0,1,0,1,0,1,0, stop=1
    # Check tx_busy is asserted after start
    await Timer(1, unit='ps')
    assert int(dut.tx_busy.value) == 1, "tx_busy should be 1 during transmission"

    # Sample the bits
    received = await uart_monitor(dut, BAUD_DIV)
    assert received == 0x55, f"Expected 0x55, got 0x{received:02X}"

    await Timer(1, unit='ps')
    assert int(dut.tx.value) == 1, "tx should be high after transmission"
    assert int(dut.tx_busy.value) == 0, "tx_busy should be 0 after transmission"

