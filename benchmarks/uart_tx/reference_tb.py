@cocotb.test()
async def test_tx_busy_behavior(dut):
    """Verify tx_busy=1 during transmission and 0 after completion."""
    clock = Clock(dut.clk, 10, unit='ns')
    cocotb.start_soon(clock.start())

    dut.rst_n.value = 0
    await RisingEdge(dut.clk)
    await RisingEdge(dut.clk)
    await RisingEdge(dut.clk)
    dut.rst_n.value = 1
    await RisingEdge(dut.clk)

    await send_byte(dut, 0xA5)

    # Immediately after start, tx_busy should be 1
    await Timer(1, unit='ps')
    assert int(dut.tx_busy.value) == 1, "tx_busy should be 1 during transmission"

    # Wait for transmission to complete and verify tx_busy goes to 0
    received = await uart_monitor(dut, BAUD_DIV)
    await Timer(1, unit='ps')
    assert received == 0xA5, f"Expected 0xA5, got 0x{received:02X}"
    assert int(dut.tx_busy.value) == 0, "tx_busy should be 0 after transmission completes"
    assert int(dut.tx.value) == 1, "tx should idle high after transmission"
