"""
reference_tb.py
Hand-written reference testbench for APB Slave (apb_slave).
Ground truth -- never regenerated by LLM.

Implements basic APB transfer protocol for testing:
  - Setup phase: PSEL=1, PENABLE=0 (1 cycle)
  - Access phase: PSEL=1, PENABLE=1 (1 cycle)
  - Idle phase: PSEL=0, PENABLE=0

CRITICAL -- correct cocotb 2.x synchronous pattern:
  - Drive signals IMMEDIATELY AFTER a RisingEdge, never before
  - Use Timer(1, unit='ps') after RisingEdge for VPI settling
"""
import cocotb
from cocotb.clock import Clock
from cocotb.triggers import RisingEdge, Timer


async def apb_write(dut, addr, data):
    """APB write transfer to given word-aligned address."""
    # Setup phase
    await RisingEdge(dut.PCLK)
    dut.PSEL.value = 1
    dut.PENABLE.value = 0
    dut.PWRITE.value = 1
    dut.PADDR.value = addr
    dut.PWDATA.value = data
    # Access phase
    await RisingEdge(dut.PCLK)
    dut.PENABLE.value = 1
    # Complete
    await RisingEdge(dut.PCLK)
    dut.PSEL.value = 0
    dut.PENABLE.value = 0


async def apb_read(dut, addr):
    """APB read transfer from given word-aligned address. Returns data."""
    # Setup phase
    await RisingEdge(dut.PCLK)
    dut.PSEL.value = 1
    dut.PENABLE.value = 0
    dut.PWRITE.value = 0
    dut.PADDR.value = addr
    # Access phase
    await RisingEdge(dut.PCLK)
    dut.PENABLE.value = 1
    # Sample PRDATA at end of access phase
    await Timer(1, unit='ps')
    data = int(dut.PRDATA.value)
    pslverr = int(dut.PSLVERR.value)
    # Complete
    await RisingEdge(dut.PCLK)
    dut.PSEL.value = 0
    dut.PENABLE.value = 0
    return data, pslverr


async def apb_reset(dut):
    """Assert PRESETn for 3 clock cycles, then release."""
    dut.PRESETn.value = 0
    dut.PSEL.value = 0
    dut.PENABLE.value = 0
    dut.PWRITE.value = 0
    dut.PADDR.value = 0
    dut.PWDATA.value = 0
    await RisingEdge(dut.PCLK)
    await RisingEdge(dut.PCLK)
    await RisingEdge(dut.PCLK)
    dut.PRESETn.value = 1
    await RisingEdge(dut.PCLK)


@cocotb.test()
async def test_reset(dut):
    """After reset, verify PRDATA=0, PREADY=1, PSLVERR=0."""
    clock = Clock(dut.PCLK, 10, unit='ns')
    cocotb.start_soon(clock.start())

    await apb_reset(dut)

    await Timer(1, unit='ps')
    assert int(dut.PRDATA.value) == 0, "PRDATA should be 0 after reset"
    assert int(dut.PREADY.value) == 1, "PREADY should be 1"
    assert int(dut.PSLVERR.value) == 0, "PSLVERR should be 0 after reset"


@cocotb.test()
async def test_write_read_reg0(dut):
    """Write 0xDEADBEEF to address 0x00, read back and verify."""
    clock = Clock(dut.PCLK, 10, unit='ns')
    cocotb.start_soon(clock.start())

    await apb_reset(dut)

    await apb_write(dut, 0x00, 0xDEADBEEF)
    data, err = await apb_read(dut, 0x00)

    assert err == 0, f"PSLVERR should be 0 for reg0, got {err}"
    assert data == 0xDEADBEEF, f"Expected 0xDEADBEEF, got 0x{data:08X}"


@cocotb.test()
async def test_write_read_all_regs(dut):
    """Write different values to all 4 registers, read all back."""
    clock = Clock(dut.PCLK, 10, unit='ns')
    cocotb.start_soon(clock.start())

    await apb_reset(dut)

    # Write all 4 registers
    write_data = {
        0x00: 0x12345678,
        0x04: 0xABCDEF01,
        0x08: 0xDEADBEEF,
        0x0C: 0xCAFEBABE,
    }

    for addr, data in write_data.items():
        await apb_write(dut, addr, data)

    # Read all 4 registers back
    for addr, expected in write_data.items():
        data, err = await apb_read(dut, addr)
        assert err == 0, f"PSLVERR should be 0 for addr 0x{addr:02X}, got {err}"
        assert data == expected, (
            f"Addr 0x{addr:02X}: expected 0x{expected:08X}, got 0x{data:08X}"
        )


@cocotb.test()
async def test_invalid_addr(dut):
    """Read from invalid address 0xFF — verify PSLVERR=1."""
    clock = Clock(dut.PCLK, 10, unit='ns')
    cocotb.start_soon(clock.start())

    await apb_reset(dut)

    data, err = await apb_read(dut, 0xFF)
    assert err == 1, f"PSLVERR should be 1 for invalid addr 0xFF, got {err}"
    assert data == 0, "PRDATA should be 0 for invalid address"


@cocotb.test()
async def test_read_before_write(dut):
    """Read from all registers before writing — verify 0 (reset value)."""
    clock = Clock(dut.PCLK, 10, unit='ns')
    cocotb.start_soon(clock.start())

    await apb_reset(dut)

    for addr in [0x00, 0x04, 0x08, 0x0C]:
        data, err = await apb_read(dut, addr)
        assert err == 0, f"PSLVERR should be 0 for addr 0x{addr:02X}"
        assert data == 0, (
            f"Addr 0x{addr:02X}: expected 0 before write, got 0x{data:08X}"
        )
